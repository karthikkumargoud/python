
import pandas as pd

s1 = pd.Series([10, 20, 30, 40, 50])
s2 = pd.Series([2, 4, 6, 8, 10])

print("Series 1:")
print(s1)

print("\nSeries 2:")
print(s2)

print("\nAddition of two Series:")
print(s1 + s2)

print("\nSubtraction of two Series:")
print(s1 - s2)

print("\nMultiplication of two Series:")
print(s1 * s2)

print("\nDivision of two Series:")
print(s1 / s2)

